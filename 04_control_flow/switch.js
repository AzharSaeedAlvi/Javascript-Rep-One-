const month = 3 

switch (month) {

    case 1:
        console.log("January")
        break;
    case 2:
        console.log("Feburary")
        break;
    case 3: 
        console.log("March")
        // break;
    case "april":
        console.log("April")
        break;
   
    default:
        console.log("default case matched")
        
        break;
}


// NOTE: By defualt, as soon as a key is matched the whole code gets executed except the default section. However, when we use break the code stops there and comes out of the control flow. 

